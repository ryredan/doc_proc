    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int result = pdfFolderChooser.showOpenDialog(this);
        if(result == pdfFolderChooser.APPROVE_OPTION){
            jTextField1.setText(pdfFolderChooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        File[] files = new File(jTextField1.getText()).listFiles(new FilenameFilter() {
            @Override
            public boolean accept(File dir, String name) {
                return name.toLowerCase().endsWith(".pdf");
            }
        });
        for (File file : files) {
            Object[] arquivoAtual = this.docIde.checkDocumentType(file);
            ((DefaultListModel<String>)jList1.getModel()).addElement(String.format("%s: %s (%.2f%%)", file.getName(), arquivoAtual[0], arquivoAtual[1]));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        int result = dictionaryChooser.showOpenDialog(this);
        if(result == dictionaryChooser.APPROVE_OPTION){
            this.docIde.loadDictionary(dictionaryChooser.getSelectedFile());
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

